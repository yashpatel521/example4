            <div class="artist-bio-content">
                <div class="artist-bio-intro">
                    <p>
                        As the pseudonymous artistic duo describe: TARWUK is a
                        "condition", and this "condition" is their practice. Foregoing
                        their individual identities to inhabit a single artistic entity,
                        TARWUK refer to the 1960s and 70s avant-garde collectives of
                        their native, former Yugoslavia, for whom collaboration was both
                        method and ideology. Driven by an existential enquiry into the
                        boundaries of the self, TARWUK is firstly a "research into
                        selfhood", an experimental union that strains the limit of what
                        artmaking can be. Revisionist by nature and diverse in output,
                        their multi-faceted practice draws upon a wide range of
                        functions and fragments. Though their work may involve painting,
                        sculpture, installation and performance, "mediums are different
                        venues to explore a collaborative approach; they're not ends in
                        and of themselves."
                    </p>
                </div>

                <div class="artist-bio-extra" id="artist-bio-extra">
                    <p>
                        Their move from Eastern Europe to New York City in the early
                        2010s – and the marked change in social tenor and political
                        timbre that accompanied it – may account for TARWUK's
                        interrogations of identity and their affinity with the
                        travelling fragment. Taking cues from the radical dramatist
                        Bertolt Brecht, whose oeuvre is characterised by its fragmentary
                        nature, TARWUK work with the piecemeal contents of culture:
                        allegories, symbols, recurring motifs. In sculptural works,
                        techniques of montage and assemblage can be found, bringing
                        together disparate pieces while emphasising contradiction and
                        adjunction.
                    </p>
                    <p>
                        As part of an artistic vocabulary befitting an unstable and
                        homeless contemporary, their working strategy prioritises
                        endless recombination and encourages the creation of other, new
                        forms. If Brecht's life's work contended that reality is only
                        graspable through a world of interconnections, TARWUK, in turn,
                        seeks to foster such a world.
                    </p>
                </div>

